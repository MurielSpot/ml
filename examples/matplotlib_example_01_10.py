# 画了三个图形。
import matplotlib.pyplot as plt
import numpy as np

x=np.linspace(0,50,20)
y=np.linspace(0,50,20)

x2=np.random.normal(0,10,[20])
y2=np.random.normal(0,10,[20])

plt.show()
plt.plot(x,y,x2,y2,x+10,y)
# x,y画出一条线；
# x2,y2画出折线，但仍然是连在一起的；
# x+10,y画出一条线，即x,y对应的线向左平移了10个单位。

